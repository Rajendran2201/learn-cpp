/*

NOTE: 

>> Extraction Operator

<< Insertion Operator

*/


#include <iostream>
using namespace std;

int main(){

  string name;
  cout << "Enter your name: ";
  cin >> name;

  cout << "Hello " << name << ", Nice to meet you!";

  return 0;
}

/*

Output: 

Enter your name: Manoj
Hello Manoj, Nice to meet you!%  

*/